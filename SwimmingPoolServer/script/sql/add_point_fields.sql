-- ===================================================================
-- 添加积分字段
-- 为订单表和会员卡表添加积分相关字段
-- ===================================================================

-- 1. 为订单表添加获得积分字段
-- --------------------
ALTER TABLE sp_order ADD COLUMN get_point BIGINT(20) DEFAULT 0 COMMENT '获得积分（元）' AFTER refund_reason;

-- 2. 为会员卡表添加积分余额字段
-- --------------------
ALTER TABLE sp_member_card ADD COLUMN total_point BIGINT(20) DEFAULT 0 COMMENT '积分余额' AFTER total_consume;

-- 3. 验证字段添加
-- --------------------
-- 查看订单表结构
SELECT '订单表字段：' as info;
DESCRIBE sp_order;

-- 查看会员卡表结构
SELECT '会员卡表字段：' as info;
DESCRIBE sp_member_card;

-- ===================================================================
-- 说明：
-- 1. get_point: 订单获得的积分（元），支付成功后更新
-- 2. total_point: 会员卡积分余额（分），支付成功后累加
-- 3. 积分计算规则：实际消费金额（元）= 获得积分（分）
--    例如：消费50元，获得50积分
-- ===================================================================
